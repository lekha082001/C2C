export class MathOps {
    public static Add(a:number, b:number){
        return a+b;
    }

    public static Subtract(a:number, b:number){
        return a-b;
    }

    public static Multiply(a:number, b:number){
        return a*b;
    }

    public static Divide(a:number, b:number){
        return a/b;
    }
}
